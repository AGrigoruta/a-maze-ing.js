    load() {
        // Init canvas
        this.stage = new createjs.Stage("Irinagame");

        // Load assets
        const queue = new createjs.LoadQueue();
        const that = this;
        queue.addEventListener('complete', () => {
            that.tilesImgs.wall = queue.getResult('tile_wall');
            that.setup();
        });
        queue.loadManifest([
            { id: 'tile_wall', src: 'img/tile_wall.png' }
        ]);
    }

    setup() {
        // Reset environment states
        this.tiles = [];
        this.grassTiles = [];
        this.towerEdgeTiles = [];

        // Draw tiles
        this.drawTiles();

        // Start loop
        if (!createjs.Ticker.hasEventListener('tick')) {
            createjs.Ticker.addEventListener('tick', gGameEngine.update);
            createjs.Ticker.setFPS(this.fps);
        }
    }

    update() {
        // Stage
        gGameEngine.stage.update();
    }

    drawTiles() {
        const tile = new Tile('grass', { x: 0, y: 0 });
        this.stage.addChild(tile.bmp);
        this.tiles.push(tile);
    }
